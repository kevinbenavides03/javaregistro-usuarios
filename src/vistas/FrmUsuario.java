    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

        BasicDBObject findTestItemQuery = new BasicDBObject();
        findTestItemQuery.put("nombre", "'" + txtNombre.getText() + "'");
        DBCursor testItemsCursor = tabla.find(findTestItemQuery);

        if (txtEdad.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese edad");
            txtEdad.requestFocus();
            return;
        }
        if (txtSexo.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese sexo");
            txtSexo.requestFocus();
            return;
        }
        if (txtGenMus.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Generos Musicales");
            txtGenMus.requestFocus();
            return;
        }
        if (txtGenPel.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Generos de Peliculas favoritas");
            txtGenPel.requestFocus();
            return;
        }

        if (txtContrasenia.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese contrasenia");
            txtContrasenia.requestFocus();
            return;
        }

        this.setContra(txtContrasenia.getText());
        if (testItemsCursor.hasNext()) {
            DBObject testCodeItem = testItemsCursor.next();
            testCodeItem.put("edad", Integer.parseInt(txtEdad.getText()));
            testCodeItem.put("sexo", "'" + txtSexo.getText() + "'");
            testCodeItem.put("direccion", "'" + txtGenMus.getText() + "'");
            testCodeItem.put("sucursal_preferencia", "'" + txtGenPel.getText() + "'");
            testCodeItem.put("contrasenia", "'" + txtContrasenia.getText() + "'");
            tabla.save(testCodeItem);

            JOptionPane.showMessageDialog(null, "Datos Actualizados");
            cargarInformacion();
        } else {
            JOptionPane.showMessageDialog(null, "Error,, no se pudieron actualizar los datos");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        if (txtContrasenia.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese contrasenia");
            txtContrasenia.requestFocus();
            return;
        }
        
        BasicDBObject deleteQuery = new BasicDBObject();
        deleteQuery.put("nombre", "'" + txtNombre.getText() + "'");
        deleteQuery.put("contrasenia", "'" + txtContrasenia.getText()+ "'");
        DBCursor cursor = tabla.find(deleteQuery);
        while (cursor.hasNext()) {
            DBObject item = cursor.next();
            tabla.remove(item);
            JOptionPane.showMessageDialog(null, "Usuario eliminado,usted saldra de Registro usuarios K.O");
            FrmInicioSesion inicio= new FrmInicioSesion();
            inicio.setVisible(true);
            this.dispose();
        }

        cargarInformacion();
    }//GEN-LAST:event_btnEliminarActionPerformed

